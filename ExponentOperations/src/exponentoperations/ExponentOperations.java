package exponentoperations;

/**
 *
 * @author Darko Frederick
 */
public class ExponentOperations {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // EXPONENTS OPERATIONS 
        System.out.println(Math.pow(2,3));
        System.out.println(Math.pow(4,0.5));
        System.out.println(Math.pow(2.5,2));
        System.out.println(Math.pow(2.5,-2));
        
        // results of the following remainders 
        System.out.println(56%6);
        System.out.println(78%-4);
        System.out.println(-34%5);
        System.out.println(-34%-5);
        System.out.println(5%1);
        System.out.println(1%5);
    }
    
}
